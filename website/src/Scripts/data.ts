export const getAnimalInfo = (key: AnimalKeysCertian): AnimalInfo => {
  if (key.indexOf("elephant") >= 0) {
    return elephantInfo;
  } else if (key.indexOf("gorilla") >= 0) {
    return gorillaInfo;
  } else if (key.indexOf("rhino") >= 0) {
    return rhinoInfo;
  } else if (key.indexOf("tiger") >= 0) {
    return tigerInfo;
  } else {
    return {
      title: "Animal Not Found",
      info:
        "Unfortunatly, due to time and training cost constraints, the animal you scanned is not recognized. Please try scanning one of the following supported animals:",
      animalsToTry: ["Elephant", "Gorilla", "Rhino", "Tiger"],
    };
  }
};
